package com.isoftstone.web.pojo;

public class RouteFitRate {
	private int numOfNotPass;
	private int numOfOverload;
	private double fitRate;
	public int getNumOfNotPass() {
		return numOfNotPass;
	}
	public void setNumOfNotPass(int numOfNotPass) {
		this.numOfNotPass = numOfNotPass;
	}
	public int getNumOfOverload() {
		return numOfOverload;
	}
	public void setNumOfOverload(int numOfOverload) {
		this.numOfOverload = numOfOverload;
	}
	public double getFitRate() {
		return fitRate;
	}
	public void setFitRate(double fitRate) {
		this.fitRate = fitRate;
	}
	
	
}
