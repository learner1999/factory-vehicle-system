package com.isoftstone.web.algorithm;

public class Emlop {
	private int sid;//站点id
	private int scount;//在此站点的人数
	private double x;//站点横坐标
	private double y;//站点纵坐标
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public int getScount() {
		return scount;
	}
	public void setScount(int scount) {
		this.scount = scount;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
}
