package com.isoftstone.web.algorithm;

import java.util.*;

public class Route {
	private int station[];
	private int man;
	private int deep;
	
	
	public int[] getStation() {
		return station;
	}
	public void setStation(int[] station) {
		this.station = station;
	}
	public int getMan() {
		return man;
	}
	public void setMan(int man) {
		this.man = man;
	}
	public int getDeep() {
		return deep;
	}
	public void setDeep(int deep) {
		this.deep = deep;
	}
	
}
