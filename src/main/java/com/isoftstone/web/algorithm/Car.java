package com.isoftstone.web.algorithm;

public class Car {
	private int c_id;
	private String c_brand;
	private int c_seat;
	private int isused=0;
	
	
	
	public int getIsused() {
		return isused;
	}
	public void setIsused(int isused) {
		this.isused = isused;
	}
	public int getC_id() {
		return c_id;
	}
	public void setC_id(int c_id) {
		this.c_id = c_id;
	}
	public String getC_brand() {
		return c_brand;
	}
	public void setC_brand(String c_brand) {
		this.c_brand = c_brand;
	}
	public int getC_seat() {
		return c_seat;
	}
	public void setC_seat(int c_seat) {
		this.c_seat = c_seat;
	}
	
	
}
